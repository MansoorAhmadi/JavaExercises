public class EvenIntegers {
    public static void main(String[] args) {
        //Even operation
        //write an algorithm to print even integers only until 99




        for (int i=5; i<=99; i++){

            //expressions are always inside parenthesis (i%2)
            if ((i%2) ==0 ){
                System.out.println(i);
            }
        }
    }
}
