package basics;

public class IntegerDivision {

    /**
     * / Result of Integer Division
     */

    public static void main(String[] args) {
        int i1 = 5;
        int i2 = 2;
        int i3 = i1 / i2;
        System.out.println(i3); // i3 == 2

        // Test
        double i0 = 2.9;
        i3 = (int) i0;
        System.out.println(i3);
    }

}
